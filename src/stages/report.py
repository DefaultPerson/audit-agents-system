async def send_poc_results(
    address: str,
    chain: Chain,
    findings: list[VulnerabilityFinding],
) -> bool:
    """Send PoC verification results to Telegram."""
    verified = [f for f in findings if f.verified]
    critical = [f for f in findings if f.severity == Severity.CRITICAL]
    failed = [f for f in critical if not f.verified]

    if not critical:
        return False  # Nothing to report

    chain_config = get_chain_config(chain.value)
    short_addr = f"{address[:6]}...{address[-4:]}"

    lines = [
        "🧪 *PoC Verification Complete*",
        "",
        f"Contract: `{short_addr}`",
        f"Chain: {chain_config.name}",
        "",
        f"Results: *{len(verified)}/{len(critical)}* exploitable",
    ]

    if verified:
        lines.append("")
        lines.append("✅ *Verified Exploits:*")
        for f in verified[:5]:  # Limit to 5
            title = f.title[:45] if len(f.title) > 45 else f.title
            lines.append(f"• {title}")

    if failed and len(failed) <= 5:
        lines.append("")
        lines.append("❌ *Not Exploitable:*")
        for f in failed:
            title = f.title[:45] if len(f.title) > 45 else f.title
            lines.append(f"• {title}")
    elif failed:
        lines.append("")
        lines.append(f"❌ *Not Exploitable:* {len(failed)} findings")

    return await send_telegram_message("\n".join(lines))


async def generate_initial_report(
    report: AuditReport,
    decompiled_code: str = "",
) -> tuple[str, bool]:
    """
    Generate initial report with optional PoC button for CRITICAL findings.

    This is the first report sent after ANALYZE stage.
    If CRITICAL findings exist, includes a button to trigger PoC verification.

    Args:
        report: Audit report object
        decompiled_code: Decompiled contract code for PoC generation

    Returns:
        (report_path, notified)
    """
    # Save report
    report_path = save_report(report)

    # Send notification based on status
    notified = False

    if report.status == AuditResult.VULNERABLE:
        has_critical = report.findings_count.critical > 0
        has_high = report.findings_count.high > 0

        if has_critical:
            # Use new telegram_bot with PoC button
            try:
                from ..telegram_bot import send_initial_report as send_with_button

                success, _ = await send_with_button(
                    report, report_path, decompiled_code
                )
                notified = success
            except ImportError:
                # Fallback to old method if telegram_bot not available
                notified = await send_critical_alert(report, report_path)

        elif has_high:
            # HIGH findings without PoC button
            notified = await send_critical_alert(report, report_path)
        else:
            # Medium/Low findings - text notification
            chain_config = get_chain_config(report.chain.value)
            message = (
                f"🔍 *Vulnerability Found*\n\n"
                f"*Address*: `{report.address}`\n"
                f"*Chain*: {chain_config.name}\n"
                f"*Findings*: {len(report.findings)} (Medium/Low)\n\n"
                f"Report saved: {Path(report_path).name}"
            )
            notified = await send_telegram_message(message)

    elif report.status == AuditResult.CLEAN:
        # Don't spam Telegram with clean audits
        log.debug(
            "Clean audit for %s - skipping Telegram notification", report.address[:16]
        )
        notified = False

    elif report.status == AuditResult.ERROR:
        notified = await send_error_notification(
            report.address, report.chain, report.error_message or "Unknown error"
        )

    return report_path, notified


async def generate_and_notify(report: AuditReport) -> tuple[str, bool]:
    """
    Full report flow: generate, save, and notify.

    DEPRECATED: Use generate_initial_report() for new code.
    Kept for backward compatibility.
    """
    return await generate_initial_report(report, decompiled_code="")


def build_report(
    address: str,
    chain: Chain,
    started_at: datetime | str,
    status: AuditResult,
    findings: list[VulnerabilityFinding],
    rag_context_used: bool,
    error_message: str | None = None,
) -> AuditReport:
    """Build audit report object."""
    findings_count = FindingsCount(
        critical=sum(1 for f in findings if f.severity == Severity.CRITICAL),
        high=sum(1 for f in findings if f.severity == Severity.HIGH),
        medium=sum(1 for f in findings if f.severity == Severity.MEDIUM),
        low=sum(1 for f in findings if f.severity == Severity.LOW),
        info=sum(1 for f in findings if f.severity == Severity.INFO),
    )

    total_profit = sum(f.estimated_profit or 0 for f in findings)
    started_at_dt = (
        datetime.fromisoformat(started_at) if isinstance(started_at, str) else started_at
    )

    return AuditReport(
        address=address,
        chain=chain,
        startedAt=started_at_dt,
        completedAt=datetime.now(UTC),
        findings=findings,
        findingsCount=findings_count,
        totalProfitUsd=total_profit if total_profit > 0 else None,
        status=status,
        errorMessage=error_message,
        ragContextUsed=rag_context_used,
    )
