log = logging.getLogger(__name__)

# Severity emojis
SEVERITY_EMOJI = {
    Severity.CRITICAL: "🔴",
    Severity.HIGH: "🟠",
    Severity.MEDIUM: "🟡",
    Severity.LOW: "🔵",
    Severity.INFO: "⚪",
}


def get_report_filename() -> str:
    """Generate markdown report filename."""
    return "final.md"


def generate_report_markdown(report: AuditReport) -> str:
    """Generate markdown report content."""
    chain_config = get_chain_config(report.chain.value)
    explorer_url = f"{chain_config.explorer_url}/address/{report.address}"

    md = f"""# Audit Report: {report.address}

## Summary

| Field | Value |
|-------|-------|
| **Address** | [`{report.address}`]({explorer_url}) |
| **Chain** | {chain_config.name} |
| **Started** | {report.started_at} |
| **Completed** | {report.completed_at} |
| **Status** | {report.status.value.upper()} |
| **RAG Context** | {"Yes" if report.rag_context_used else "No"} |

## Findings Summary

| Severity | Count |
|----------|-------|
| {SEVERITY_EMOJI[Severity.CRITICAL]} Critical | {report.findings_count.critical} |
| {SEVERITY_EMOJI[Severity.HIGH]} High | {report.findings_count.high} |
| {SEVERITY_EMOJI[Severity.MEDIUM]} Medium | {report.findings_count.medium} |
| {SEVERITY_EMOJI[Severity.LOW]} Low | {report.findings_count.low} |
| {SEVERITY_EMOJI[Severity.INFO]} Info | {report.findings_count.info} |

"""

    if report.total_profit_usd:
        md += f"**Estimated Total Profit**: ${report.total_profit_usd:,.2f}\n\n"

    if not report.findings:
        md += "## Findings\n\nNo vulnerabilities found.\n"
    else:
        md += "## Findings\n\n"

        for finding in report.findings:
            md += f"""### {SEVERITY_EMOJI[finding.severity]} {finding.title}

**ID**: `{finding.id}`
**Type**: {finding.type.value}
**Severity**: {finding.severity.value.upper()}
**Confidence**: {finding.confidence * 100:.0f}%
**Source**: {finding.source.value}
**Verified**: {"Yes" if finding.verified else "No"}

#### Description

{finding.description}

#### Impact

{finding.impact}

"""

            if finding.location.function or finding.location.selector:
                md += f"""#### Location

- **Function**: {finding.location.function or "Unknown"}
- **Selector**: `{finding.location.selector or "N/A"}`
{f"- **Line**: {finding.location.line}" if finding.location.line else ""}

"""

            if finding.exploit_scenario:
                md += f"""#### Exploit Scenario

{finding.exploit_scenario}

"""

            if finding.estimated_profit:
                md += f"""#### Estimated Profit

${finding.estimated_profit:,.2f}

"""

            if finding.poc_code:
                md += f"""#### PoC Code

```solidity
{finding.poc_code}
```

"""

            md += "---\n\n"

    if report.error_message:
        md += f"## Errors\n\n```\n{report.error_message}\n```\n"

    md += "\n---\n*Generated by Smart Contract Audit Pipeline*\n"

    return md


def save_report(report: AuditReport) -> str:
    """Save report to file."""
    audit_dir = get_audit_dir(report.chain.value, report.address)
    reports_dir = audit_dir / "reports"
    reports_dir.mkdir(parents=True, exist_ok=True)

    filename = get_report_filename()
    filepath = reports_dir / filename
    content = generate_report_markdown(report)

    filepath.write_text(content)
    log.info("Report saved: %s", filepath)

    return str(filepath)


async def send_telegram_message(text: str, parse_mode: str = "Markdown") -> bool:
    """Send text message via Telegram."""
    bot_token = settings.telegram_bot_token
    chat_id = settings.telegram_chat_id

    if not bot_token or not chat_id:
        log.debug("Telegram not configured, skipping notification")
        return False

    for attempt in range(3):
        try:
            async with httpx.AsyncClient(timeout=30) as client:
                response = await client.post(
                    f"https://api.telegram.org/bot{bot_token}/sendMessage",
                    json={
                        "chat_id": chat_id,
                        "text": text,
                        "parse_mode": parse_mode,
                        "disable_web_page_preview": True,
                    },
                )

                data = response.json()
                if data.get("ok"):
                    return True

                log.warning(
                    "Telegram error (attempt %d): %s",
                    attempt + 1,
                    data.get("description"),
                )

        except Exception as e:
            log.warning("Telegram request failed (attempt %d): %s", attempt + 1, e)

        if attempt < 2:
            await asyncio.sleep(1 * (attempt + 1))

    return False


async def send_telegram_document(filepath: str, caption: str) -> bool:
    """Send document (MD file) via Telegram."""
    bot_token = settings.telegram_bot_token
    chat_id = settings.telegram_chat_id

    if not bot_token or not chat_id:
        log.debug("Telegram not configured, skipping document upload")
        return False

    file_path = Path(filepath)
    if not file_path.exists():
        log.warning("File not found: %s", filepath)
        return False

    for attempt in range(3):
        try:
            async with httpx.AsyncClient(timeout=60) as client:
                file_bytes = await asyncio.to_thread(file_path.read_bytes)
                response = await client.post(
                    f"https://api.telegram.org/bot{bot_token}/sendDocument",
                    data={
                        "chat_id": chat_id,
                        "caption": caption,
                        "parse_mode": "Markdown",
                    },
                    files={"document": (file_path.name, file_bytes, "text/markdown")},
                )

                data = response.json()
                if data.get("ok"):
                    return True

                log.warning(
                    "Telegram sendDocument error (attempt %d): %s",
                    attempt + 1,
                    data.get("description"),
                )

        except Exception as e:
            log.warning("Telegram sendDocument failed (attempt %d): %s", attempt + 1, e)

        if attempt < 2:
            await asyncio.sleep(1 * (attempt + 1))

    return False


def escape_markdown(text: str) -> str:
    """Escape special characters for Telegram MarkdownV2."""
    if not text:
        return ""
    return re.sub(r"[_*\[\]()~`>#+=|{}.!\\-]", r"\\\g<0>", text)


def build_short_caption(report: AuditReport) -> str:
    """Build short caption for Telegram document (max 1024 chars)."""
    chain_config = get_chain_config(report.chain.value)

    lines = [
        "🚨 VULNERABILITY FOUND",
        "",
        f"Address: `{report.address}`",
        f"Chain: {chain_config.name}",
    ]

    if report.findings_count.critical > 0:
        lines.append(f"🔴 Critical: {report.findings_count.critical}")
    if report.findings_count.high > 0:
        lines.append(f"🟠 High: {report.findings_count.high}")
    if report.findings_count.medium > 0:
        lines.append(f"🟡 Medium: {report.findings_count.medium}")
    if report.findings_count.low > 0:
        lines.append(f"🔵 Low: {report.findings_count.low}")

    if report.total_profit_usd:
        lines.append(f"💰 Est. Profit: ${report.total_profit_usd:,.0f}")

    # PoC verification stats
    verified = [f for f in report.findings if f.verified]
    critical = [f for f in report.findings if f.severity == Severity.CRITICAL]
    if critical:
        lines.append(f"🧪 PoC: {len(verified)}/{len(critical)} verified")

    # Add top finding titles
    top_findings = report.findings[:2]
    if top_findings:
        lines.append("")
        lines.append("Top findings:")
        for f in top_findings:
            title = f.title[:40]
            lines.append(f"• [{f.severity.value.upper()[:4]}] {title}")

    return "\n".join(lines)[:1000]


async def send_critical_alert(report: AuditReport, report_path: str) -> bool:
    """Send critical audit alert with report attachment."""
    caption = build_short_caption(report)
    return await send_telegram_document(report_path, caption)


